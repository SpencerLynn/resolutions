/*
The sequence of triangle numbers is generated by adding the natural numbers. So the 7th triangle number would be 1 + 2 + 3 + 4 + 5 + 6 + 7 = 28. The first ten terms would be:

1, 3, 6, 10, 15, 21, 28, 36, 45, 55, ...

Let us list the factors of the first seven triangle numbers:

 1: 1
 3: 1,3
 6: 1,2,3,6
10: 1,2,5,10
15: 1,3,5,15
21: 1,3,7,21
28: 1,2,4,7,14,28

We can see that 28 is the first triangle number to have over five divisors.

What is the value of the first triangle number to have over five hundred divisors?
*/


// ANSWER //

Array.prototype.last=function(){return this[this.length-1]};
Array.prototype.sum=function(){return this.reduce(function(s,n){return s+n})};

var findFactors = function (num){
    var factors = [1];
    
    var highFactor = num/2;
    var currentFactor = 2;
    
    while (currentFactor <= highFactor) {
        if (num % currentFactor === 0) {
            factors.push(currentFactor);
            factors.push(num/currentFactor);
        }
        
        currentFactor++;
        highFactor = num/currentFactor;
    }
    
    if (factors.last() != num)
        factors.push(num);
    
    return factors.sort(function(a,b){return a-b;});
};

var findTriangleNumberWithFactors = function (numFactors) {
    var triangle = [1,2];
    while (findFactors(triangle.sum()).length <= numFactors) {
        triangle.push(triangle.last() + 1);
    }
    return triangle.sum();
};

findTriangleNumberWithFactors(500);

